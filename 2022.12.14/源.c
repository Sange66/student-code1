#define _CRT_SECURE_NO_WARNINGS 1
#include<stdio.h>
int main()
{
	int a, b;
	a = 7;         //00000000000000000000000000000111-->00000000000000000000000000001110=14(正负都是直接左移，右边补0) 
	b = a << 1;
	printf("%d\n", b);
	a = -5;        //10000000000000000000000000000101-->10000000000000000000000000010100=-20(正负都是直接左移，右边补0) 
	b = a << 2;
	printf("%d\n", b);
	return 0;
}